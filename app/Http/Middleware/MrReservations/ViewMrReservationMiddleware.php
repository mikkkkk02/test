<?php

namespace App\Http\Middleware\MrReservations;

use Closure;

use App\Company;

class ViewMrReservationMiddleware
{
    /**
     * Handle an incoming request.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \Closure  $next
     * @return mixed
     */
    public function handle($request, Closure $next)
    {
        $user = $request->user();


        /* Check if super admin */
        if(!$user->isSuperUser()) {

            /* Check if user has permission */
            if(!$user->hasRole('Viewing of Meeting Room Reservations')) {
                if (!in_array($user->id, Company::getTechnicianIds())) {
                    return $this->redirectRoute($request); 
                }
            }
        }

        return $next($request);
    }

    /**
     * Handle the unauthorized redirect
     *
     * @return redirect
     */
    public function redirectRoute($request) {

        /* Check if called via ajax*/
        if($request->ajax())
            return response(['Permission' => 'Unauthorized process'], 422);

        return redirect()->route('error.unauthorized');
    }
}
